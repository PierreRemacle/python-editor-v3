def show_image(np, image, palette):
    for y in range(WIDTH):
        if y % 2 == 0:
            base = (y + 1) * WIDTH - 1
            for x in range(WIDTH):
                np[base - x] = palette[image[y * WIDTH + x]]
        else:
            base = y * WIDTH
            for x in range(WIDTH):
                np[base + x] = palette[image[y * WIDTH + x]]
    np.show()

def panel_show(np, text, palette, x=0, y=0):
    cx = x
    for char in text.upper():
        rows = FONT.get(char, FONT[' '])
        for row_idx in range(5):
            row_bits = rows[row_idx]
            for col in range(5):
                color = palette[1] if row_bits & (1 << (4 - col)) else palette[0]
                set_led(np, cx + col, y + row_idx, color)
        cx += 6
    np.show()
