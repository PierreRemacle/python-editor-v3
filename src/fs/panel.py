from microbit import *

WIDTH = 16

def stripe_index(x, y):
    if y % 2 == 0:
        return (y + 1) * WIDTH - x - 1
    else:
        return y * WIDTH + x

def set_led(np, x, y, color):
    if 0 <= x < WIDTH and 0 <= y < WIDTH:
        np[stripe_index(x, y)] = color

